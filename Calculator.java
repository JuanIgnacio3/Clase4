public class Calculator { 
    public double sumDosNum (double a, double b){
        return a + b;
    }
    
    public double resDosNum (double a, double b){
        return a - b;
    }
    
    public double divDosNum (double a, double b){
        return a / b;
    }
    
    public double mulDosNum (double a, double b){
        return a * b;
    }
}
